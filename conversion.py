import streamlit as st
import pandas as pd
import pickle

# Load the trained model
model = pickle.load(open('best_model.pkl', 'rb'))

# Function to make predictions
def predict_conversion(data):
    prediction = model.predict(data)
    return prediction

# Main Streamlit app code
def main():
    st.title("Customer Conversion Prediction")

    # Create input form for user input
    feature1 = st.text_input("Feature 1:")
    feature2 = st.text_input("Feature 2:")
    feature3 = st.text_input("Feature 3:")
    # Add more features as per your dataset

    # Create a dictionary to store user input
    input_data = {'Feature 1': [feature1],
                  'Feature 2': [feature2],
                  'Feature 3': [feature3]}
    # Convert input data to a DataFrame
    input_df = pd.DataFrame.from_dict(input_data)

    # Make predictions
    if st.button("Predict"):
        prediction = predict_conversion(input_df)

        # Display the prediction
        if prediction[0] == 0:
            st.error("Not Converted")
        else:
            st.success("Converted")

if __name__ == '__main__':
    main()